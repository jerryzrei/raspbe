def parse(data):
    if len(data) == 1:
        number, message = data[0], ''
    else:
        number, message = data[:2]
    if message:
        message = '，并留言：”{}“'.format(message)
    if len(data) >= 3:
        name = data[2]
    else:
        name = '某位没有留下名字的'

    if len(data) >= 4:
        comment = '<br>to 这位朋友：{}'.format(data[3])
    else:
        comment = ''

    return_data = '<p>{name}朋友捐献了{number}元{message}：）{comment}</p>'.format(
        name=name,
        number=number,
        message=message,
        comment=comment)
    return return_data


